"""Write the contents for the function even_or_odd(), which gets one argument n.
We can assume that the argument is a positive integer.
The function should print "even", if the argument is even, that is, it is
divisible by two. Otherwise, it should print "odd".
Complete the function by adding code to the function body."""

def even_or_odd(n):
        if n % 2 == 0:
            print('even')
        else:
            print('odd')      
